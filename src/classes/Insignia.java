package classes;

public class Insignia {
	String nome;
	String efeito;
	
	public Insignia(String nome, String efeito) {
		this.nome = nome;
		this.efeito = efeito;
	}

	public String getNome() {
		return nome;
	}

	public void setNome(String nome) {
		this.nome = nome;
	}

	public String getEfeito() {
		return efeito;
	}

	public void setEfeito(String efeito) {
		this.efeito = efeito;
	}
	
}
