package classes;

public class Spawn {
	public String spawn;
	public String rota;
	
	public Spawn(String spawn, String rota) {
		super();
		this.spawn = spawn;
		this.rota = rota;
	}

	public String getSpawn() {
		return spawn;
	}

	public void setSpawn(String spawn) {
		this.spawn = spawn;
	}

	public String getRota() {
		return rota;
	}

	public void setRota(String rota) {
		this.rota = rota;
	}
}
